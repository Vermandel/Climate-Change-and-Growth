function [lhs, rhs] = static_resid(y, x, params)
T = NaN(8, 1);
lhs = NaN(20, 1);
rhs = NaN(20, 1);
T(1) = params(3)^params(6);
T(2) = params(23)/1000/params(22);
T(3) = T(2)*y(6);
T(4) = params(10)*y(12)^params(8);
T(5) = y(9)^params(7);
T(6) = y(13)^params(22);
T(7) = y(1)^(1-params(7));
T(8) = 1/(1-params(7))*y(9)^(1-params(7))-params(10)/(1+params(8))*y(12)^(1+params(8));
lhs(1) = y(1);
rhs(1) = y(1)*(1+y(3));
lhs(2) = y(3);
rhs(2) = y(3)*(1-params(4));
lhs(3) = y(2);
rhs(3) = y(2)^(1-params(6))*T(1);
lhs(4) = y(4);
rhs(4) = y(4)*(1-params(27));
lhs(5) = y(5);
rhs(5) = max(y(4)*T(3),0);
lhs(6) = y(6);
rhs(6) = y(6)*(1-params(24));
lhs(7) = y(20);
rhs(7) = 1-params(33)+y(20)*params(33)+x(1);
lhs(8) = y(10);
rhs(8) = 1/params(9)*(1+y(3))^params(7);
lhs(9) = y(11);
rhs(9) = T(4)*T(5);
lhs(10) = y(8);
rhs(10) = y(12)*y(20)*y(14);
lhs(11) = y(11);
rhs(11) = y(20)*y(14)*(1-y(5)*(params(22)*y(15)*(1-y(13))+T(6)));
lhs(12) = y(13);
rhs(12) = (params(14)+params(34)*x(2))^(1/(params(22)-1));
lhs(13) = y(16);
rhs(13) = y(2)*y(1)*y(8)*y(4)*(1-y(13));
lhs(14) = y(8);
rhs(14) = y(9)+y(8)*y(5)*T(6);
lhs(15) = y(7);
rhs(15) = y(7)*(1-params(25))+y(16)*params(2);
lhs(16) = y(17);
rhs(16) = y(7)*params(37);
lhs(17) = y(15);
rhs(17) = params(14)+params(34)*x(2);
lhs(18) = y(14);
rhs(18) = exp(y(7)*(-params(21)));
lhs(19) = y(18);
rhs(19) = 1000*y(5)*y(15)/(y(4)+1e-8);
lhs(20) = y(19);
rhs(20) = y(2)*T(7)*T(8)+params(9)*y(19);
end
